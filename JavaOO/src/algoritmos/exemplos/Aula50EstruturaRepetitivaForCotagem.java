package algoritmos.exemplos;

public class Aula50EstruturaRepetitivaForCotagem {

	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) {
			System.out.println(i);
		}
		
		System.out.println("----------");
		
		for (int i = 4; i >= 0; i--) {
			System.out.println(i);
		}
		
	}

}
