package algoritmos.exemplos;

public class EstruturaRepetitivaFor4 {

	public static void main(String[] args) {

		int x = 4;
		int y = 0;
		
		for (int i = 0; i < x; i++) {
			System.out.println("i -> " + i);
			System.out.println("------");
			System.out.println("x -> " + x);
			y += 10;
		}
	}
}
